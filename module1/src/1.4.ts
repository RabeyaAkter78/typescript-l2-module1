// Basic Datatypes:


// premitive data types:
// string:
let Name: string = "rabeya";

// number:
let Roll: number = 123;
//boolean:
let isAdmin: boolean = true;
// undefined:
let x: undefined = undefined;
// null:
let y: null = null;

// any: 
let d;
