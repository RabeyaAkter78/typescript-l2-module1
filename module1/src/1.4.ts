// Non-premitive dataTypes:

// Array:

let friends: string[] = ['rabeya', 'riya'];

let rollList: number[] = [1, 2];


// tuple: is a special types of array . tuples maintain order. 

// tuple --> array-->order-->types of values:


let coordinate: [number, number] = [1, 2];

let ageName: [number, string, boolean] = [40, "Me.X", false];
ageName[0] = 55;
